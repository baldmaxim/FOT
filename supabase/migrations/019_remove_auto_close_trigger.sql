-- Миграция: Удаление автозакрытия назначений
-- Версия: 019
-- Дата: 2026-01-29
-- Описание: Убираем триггер автозакрытия - сотрудник может быть в нескольких местах одновременно

-- ============================================
-- 1. Удаляем триггер автозакрытия
-- ============================================
DROP TRIGGER IF EXISTS auto_close_previous_assignment ON employee_assignments;

-- Удаляем функцию (она больше не нужна)
DROP FUNCTION IF EXISTS close_previous_assignment();

-- ============================================
-- 2. Обновляем комментарий к полю is_primary
-- ============================================
COMMENT ON COLUMN employee_assignments.is_primary IS 'Информационный флаг (не влияет на автозакрытие). Сотрудник может иметь несколько активных назначений одновременно.';

-- ============================================
-- 3. Удаляем индекс по is_primary (он больше не нужен для логики)
-- ============================================
DROP INDEX IF EXISTS idx_assignments_primary;

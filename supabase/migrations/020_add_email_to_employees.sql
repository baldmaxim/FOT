-- Миграция: Добавление email в таблицу employees
-- Версия: 020
-- Дата: 2026-01-29
-- Описание: Email для связи сотрудника с пользователем системы

-- ============================================
-- 1. Добавить столбец email
-- ============================================
ALTER TABLE employees
  ADD COLUMN IF NOT EXISTS email TEXT;

-- ============================================
-- 2. Уникальный индекс (только для непустых значений)
-- ============================================
CREATE UNIQUE INDEX IF NOT EXISTS idx_employees_email
  ON employees(email) WHERE email IS NOT NULL;

-- ============================================
-- 3. Комментарий
-- ============================================
COMMENT ON COLUMN employees.email IS 'Email сотрудника для связи с user_profile';

-- 194_contractor_pass_card_hex_uid.sql
-- Полный серийник карты (CSN) с ридера — hex_uid.
--
-- Зачем: card_uid хранит только 24-битный W26 (facility,number) — при декодировании
-- из сырого UID отбрасываются старший байт длины и хвостовой паддинг. В 24 битах
-- разные физические карты могут свернуться в один W26 (коллизия: напр. пропуск 637
-- Агаева vs штатная карта Махкамова — оба 38,52418). hex_uid хранит полный серийник
-- карты и служит уникальным ключом дедупа на этапе выдачи в пул: две разные карты с
-- одинаковым W26 теперь ловятся как «коллизия W26», а не как «та же карта».
--
-- Задним числом старые пропуска не заполняются (CSN тогда не сохранялся) — NULL.

BEGIN;

ALTER TABLE public.contractor_passes
  ADD COLUMN IF NOT EXISTS card_hex_uid text NULL;

COMMENT ON COLUMN public.contractor_passes.card_hex_uid IS
  'Полный серийник карты (CSN) с ридера; уникальный ключ против коллизий 24-бит W26 в card_uid. NULL для карт, выданных до миграции 194.';

COMMIT;

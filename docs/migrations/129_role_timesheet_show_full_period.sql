-- Миграция 129: per-role переключатель «Весь месяц» в табеле.
--
-- Зачем: до этой миграции переключатель периода в табеле всегда показывал
-- три кнопки — «1–15», «16–N» и «Весь месяц» — всем, у кого есть доступ
-- к странице. Суперадмину нужна возможность скрыть «Весь месяц» для
-- отдельных ролей (например, «Руководитель строительства» / manager_obj),
-- оставив им только полумесячные периоды.
--
-- Дефолт true → поведение для всех существующих ролей не меняется.

ALTER TABLE system_roles
  ADD COLUMN IF NOT EXISTS timesheet_show_full_period BOOLEAN NOT NULL DEFAULT true;

COMMENT ON COLUMN system_roles.timesheet_show_full_period IS
  'true → пользователи роли видят кнопку «Весь месяц» в переключателе периода табеля; false → только 1–15 и 16–N.';

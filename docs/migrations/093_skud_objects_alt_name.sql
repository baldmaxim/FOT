-- 093_skud_objects_alt_name.sql
-- Альтернативное (внутреннее) обозначение объекта строительства.
-- Используется для связи с внешними источниками/учётом, где основное
-- имя объекта не совпадает с принятым в компании кодом/шифром.
-- Может быть NULL (если внутреннее обозначение не задано).

BEGIN;

ALTER TABLE public.skud_objects
  ADD COLUMN IF NOT EXISTS alt_name text;

COMMIT;

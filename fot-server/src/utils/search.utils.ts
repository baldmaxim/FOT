// Экранирует символы LIKE/ILIKE (`%`, `_`, `\`) в пользовательском вводе.
// Без экранирования паттерны вида `%a%a%a...` вызывают full-table scan на
// больших таблицах — это не SQL-инъекция (Supabase SDK параметризует значение),
// а DoS-вектор + неожиданное поведение поиска.
//
// `max` ограничивает длину итоговой строки: для UI-поиска ФИО разумно ≤64.
export function escapeLike(input: string, max = 64): string {
  return input.replace(/[%_\\]/g, c => `\\${c}`).slice(0, max);
}
